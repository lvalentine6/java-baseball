package baseball.numbers;

public class UserNumbers {
    int first;
    int second;
    int third;

    public UserNumbers(int first, int second, int third) {
        this.first = first;
        this.second = second;
        this.third = third;
    }

    public int getFirst() {
        return first;
    }

    public int getSecond() {
        return second;
    }

    public int getThird() {
        return third;
    }
}
